    private void addPessoa() throws SQLException{
        PessoaVO pes = new PessoaVO();

        pes.setPes_cpf(jTPesCpf.getText());
        pes.setPes_nome(jTNomePes.getText());

        // -- sexo --
        SexoVO sexSelecionado = (SexoVO) jCbPesSexo.getSelectedItem();
        if (sexSelecionado != null){
            System.out.println("ID: " + sexSelecionado.getSex_id());
            System.out.println("Descricao: " + sexSelecionado.getSex_descricao());
        }
        SexoVO s = new SexoVO();
        s.setSex_id(sexSelecionado.getSex_id());

        // -- cidades --
        CidadeVO cidSelecionada = (CidadeVO) jCbCidade.getSelectedItem();
        if (cidSelecionada != null){
            System.out.println("ID: " + cidSelecionada.getCid_id());
            System.out.println("Nome: " + cidSelecionada.getCid_descricao());
        }
        CidadeVO cid = new CidadeVO();
        cid.setCid_id(cidSelecionada.getCid_id());

        // -- estados --
        EstadoVO selecionado = (EstadoVO) jCbEstado.getSelectedItem();
        if (selecionado != null) {
            System.out.println("Sigla: " + selecionado.getEst_sigla());
            System.out.println("Nome: " + selecionado.getEst_descricao());
        }
        EstadoVO est = new EstadoVO();
        est.setEst_sigla(selecionado.getEst_sigla());

        // -- bairro --
        BairroVO bairroSelecionado = (BairroVO) jCbEndBairro.getSelectedItem();
        if (bairroSelecionado != null){
            System.out.println("ID: " + bairroSelecionado.getBairro_id());
            System.out.println("Nome: " + bairroSelecionado.getBairro_descricao());
        }
        BairroVO bairro = new BairroVO();
        bairro.setBairro_id(bairroSelecionado.getBairro_id());

        // -- logradouro --
        LogradouroVO logradouroSelecionado = (LogradouroVO) jCbEndBairro.getSelectedItem();
        if (logradouroSelecionado != null){
            System.out.println("ID: " + logradouroSelecionado.getLogradouro_id());
            System.out.println("Nome: " + logradouroSelecionado.getLogradouro_descricao());
        }
        LogradouroVO lograd = new LogradouroVO();
        lograd.setLogradouro_id(logradouroSelecionado.getLogradouro_id());

        EndPostalVO ep = new EndPostalVO();
        ep.setEndP_nomeRua(jTEndNomeRua.getText());
        ep.setEndP_cep(jTEndCep.getText());
        ep.setEndP_bairro(bairro);
        ep.setEndP_cidade(cid);
        ep.setEndP_estado(est);
        ep.setEndP_logradouro(lograd);

        EnderecoVO end = new EnderecoVO();
        end.setEnd_complemento(jTEndComplemento.getText());
        end.setEnd_numero(jTEndNumero.getText());
        end.setEnd_endP_id(ep);

        java.util.List<EnderecoVO> enderecos = new java.util.ArrayList<>();
        enderecos.add(end);
        pes.setEndereco(enderecos);

        TelefoneVO tel = new TelefoneVO();
        tel.setTel_codPais(jTTelCodPais.getText());
        tel.setTel_ddd(jTTelDDD.getText());
        tel.setTel_numero(jTTelNumero.getText());
        tel.setTel_pes_cpf(pes.getPes_cpf());

        pes.setPes_ativo(true);

        try {
            controller.cadastrarPessoa(pes);
            JOptionPane.showMessageDialog(null, "Pessoa cadastrada com sucesso!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar pessoa!");
            e.printStackTrace();
        }

    }


