    private void carregarEstados() throws SQLException{
        EstadoDAO estDao = new EstadoDAO();
        try {
            List<EstadoVO> estados = estDao.buscarTodosEstados();
            for (EstadoVO e : estados){
                jCbEstado.addItem(String.valueOf(e));
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this,"Erro ao carregar itens" + e);
        }
    }

